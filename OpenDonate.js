document.addEventListener("DOMContentLoaded", function() {
    var checkbox = document.getElementById('checkbox');
    var buyButton = document.getElementById('Buy');
    var errorMessage = document.getElementById('errorMessage1');
    buyButton.addEventListener('click', function() {
        // Проверяем, заполнены ли поля
        if (!checkbox.checked || !document.getElementById('Nick').value || !document.getElementById('Email').value) {
            errorMessage.style.display = 'block'; // Показываем сообщение об ошибке
        } else {
            errorMessage.style.display = 'none'; // Скрываем сообщение об ошибке
            window.location.href = 'https://www.google.com/search?client=opera-gx&q=перенаправление+спомощю+js&sourceid=opera&ie=UTF-8&oe=UTF-8'; // Перенаправляем пользователя
        }
    });
});

